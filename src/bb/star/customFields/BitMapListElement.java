public class BitMapListElement {
	private String title;
	private String bitmap;

	public void setTitle(String title) {
		this.title = title;
	}

	public String getTitle() {
		return title;
	}

	public void setBitmap(String bitmap) {
		this.bitmap = bitmap;
	}

	public String getBitmap() {
		return bitmap;
	}
}
